package Pati;

public class Pati {
	    int id;
	    String name;
	    String mobno;
	    String location;
	    String disease;
	    String bloodgroup;
	    float weight;

	    public Pati(int id, String name, String mobno, String location, String disease, String bloodgroup, float weight) {
	        this.id = id;
	        this.name = name;
	        this.mobno = mobno;
	        this.location = location;
	        this.disease = disease;
	        this.bloodgroup = bloodgroup;
	        this.weight = weight;
	    }

}
