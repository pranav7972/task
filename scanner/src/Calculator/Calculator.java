package Calculator;

public class Calculator {
    public void addition() {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c);
    }

    public void subtraction() {
        int a = 10;
        int b = 20;
        int c = b - a;
        System.out.println(c);
    }

    public void multiply() {
        int a = 10;
        int b = 20;
        int c = a * b;
        System.out.println(c);
    }
}
 